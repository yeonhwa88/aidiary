    mPOINT NUMBER(5) CHECK(mPOINT >= 0),
    mRDATE DATE DEFAULT SYSDATE,
    LEVELNO NUMBER(2),
    PRIMARY KEY (mNO),
    FOREIGN KEY (LEVELNO) REFERENCES MEMBER_LEVEL (LEVELNO)
   ); 
