    LEVELNO   NUMBER (2),
    LEVELNAME VARCHAR2(50),
    PRIMARY KEY(LEVELNO)
);
CREATE TABLE MEMBER(
    mNO NUMBER(5),
    mNAME VARCHAR2(50),
    mPW VARCHAR2(8),
